class Loop89
{
	public static void main(String args[])
	{
		
	for(int i=7;i<=56;i*=2)
	{
		for(int j=i;j<=i+1;j++)
		{
			System.out.print(j+" ");
		}
		for(int k=30;k<=58;k+=28)
		{
			for(int n=k;n<=k+1;k++)
			{
				System.out.print(k);
			}
			System.out.println();
		}
		System.out.println();
	}
	}
}	